export function getEdexcelALevelBiologyTopicById(id: string): Topic | undefined {
  return edexcelALevelBiologyTopics.find((topic) => topic.id === id);
}

export function getEdexcelALevelBiologyTopicsByPaper(paper: 1 | 2 | 3): Topic[] {
  const paperName = `Paper ${paper}`;
  return edexcelALevelBiologyTopics.filter((topic) => topic.paperRestriction === paperName);
}

export function countEdexcelALevelBiologySubtopics(): number {
  return edexcelALevelBiologyTopics.reduce((total, topic) => total + topic.subtopics.length, 0);
}
