export function getOCRBiologyTopicById(id: string): Topic | undefined {
  return ocrBiologyTopics.find((topic) => topic.id === id);
}

export function getOCRBiologyTopicsByPaper(paper: '1/3' | '2/4' | 'Both'): Topic[] {
  if (paper === 'Both') {
    return ocrBiologyTopics.filter((topic) => topic.paperRestriction === 'Both');
  }
  return ocrBiologyTopics.filter((topic) => topic.paperRestriction === `Paper ${paper}`);
}

export function countOCRBiologySubtopics(): number {
  return ocrBiologyTopics.reduce((total, topic) => total + topic.subtopics.length, 0);
}
