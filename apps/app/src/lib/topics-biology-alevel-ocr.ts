export function getOCRALevelBiologyTopicById(id: string): Topic | undefined {
  return ocrALevelBiologyTopics.find((topic) => topic.id === id);
}

export function getOCRALevelBiologyTopicsByPaper(paper: 1 | 2): Topic[] {
  const paperName = `Paper ${paper}`;
  return ocrALevelBiologyTopics.filter((topic) => topic.paperRestriction === paperName);
}

export function countOCRALevelBiologySubtopics(): number {
  return ocrALevelBiologyTopics.reduce((total, topic) => total + topic.subtopics.length, 0);
}
