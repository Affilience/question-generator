// Helper functions
export function getEdexcelALevelPhysicsTopicById(id: string): Topic | undefined {
  return edexcelALevelPhysicsTopics.find(topic => topic.id === id);
}

export function getEdexcelALevelPhysicsTopicsByPaper(paper: 1 | 2 | 3): Topic[] {
  return edexcelALevelPhysicsTopics.filter(topic =>
    topic.paperRestriction?.includes(`Paper ${paper}`)
  );
}

export function countEdexcelALevelPhysicsSubtopics(): number {
  return edexcelALevelPhysicsTopics.reduce((total, topic) => total + topic.subtopics.length, 0);
}
