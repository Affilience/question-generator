// Helper functions
export function getAQAALevelPhysicsTopicById(id: string): Topic | undefined {
  return aqaALevelPhysicsTopics.find(topic => topic.id === id);
}

export function getAQAALevelPhysicsTopicsByPaper(paper: 1 | 2 | 3): Topic[] {
  return aqaALevelPhysicsTopics.filter(topic =>
    topic.paperRestriction?.includes(`Paper ${paper}`)
  );
}

export function getAQAALevelPhysicsOptionalTopics(): Topic[] {
  return aqaALevelPhysicsTopics.filter(topic =>
    topic.paperRestriction?.includes('Optional')
  );
}

export function countAQAALevelPhysicsSubtopics(): number {
  return aqaALevelPhysicsTopics.reduce((total, topic) => total + topic.subtopics.length, 0);
}
