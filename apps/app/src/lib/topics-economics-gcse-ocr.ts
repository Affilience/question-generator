export function getOCREconomicsTopicById(id: string): Topic | undefined {
  return ocrEconomicsTopics.find((topic) => topic.id === id);
}

export function getOCREconomicsTopicsByComponent(component: 1 | 2): Topic[] {
  const componentName = `Component ${component}`;
  return ocrEconomicsTopics.filter((topic) => topic.paperRestriction === componentName);
}

export function countOCREconomicsSubtopics(): number {
  return ocrEconomicsTopics.reduce((total, topic) => total + topic.subtopics.length, 0);
}
