export function getEdexcelBiologyTopicById(id: string): Topic | undefined {
  return edexcelBiologyTopics.find((topic) => topic.id === id);
}

export function getEdexcelBiologyTopicsByPaper(paper: 1 | 2): Topic[] {
  const paperName = `Paper ${paper}`;
  return edexcelBiologyTopics.filter((topic) => topic.paperRestriction === paperName);
}

export function countEdexcelBiologySubtopics(): number {
  return edexcelBiologyTopics.reduce((total, topic) => total + topic.subtopics.length, 0);
}
