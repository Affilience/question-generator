export function getOCRALevelComputerScienceTopicById(id: string): Topic | undefined {
  return ocrALevelComputerScienceTopics.find((topic) => topic.id === id);
}

export function getOCRALevelComputerScienceTopicsByPaper(paper: 1 | 2): Topic[] {
  const paperName = `Paper ${paper}`;
  return ocrALevelComputerScienceTopics.filter((topic) => topic.paperRestriction === paperName);
}

export function countOCRALevelComputerScienceSubtopics(): number {
  return ocrALevelComputerScienceTopics.reduce((total, topic) => total + topic.subtopics.length, 0);
}
