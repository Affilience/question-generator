export function getAQACombinedScienceTopicById(id: string): Topic | undefined {
  return aqaCombinedScienceTopics.find((topic) => topic.id === id);
}

export function getAQACombinedScienceTopicsByPaper(science: 'biology' | 'chemistry' | 'physics', paper: 1 | 2): Topic[] {
  const paperName = `${science.charAt(0).toUpperCase() + science.slice(1)} Paper ${paper}`;
  return aqaCombinedScienceTopics.filter((topic) => topic.paperRestriction === paperName);
}

export function countAQACombinedScienceSubtopics(): number {
  return aqaCombinedScienceTopics.reduce((total, topic) => total + topic.subtopics.length, 0);
}
