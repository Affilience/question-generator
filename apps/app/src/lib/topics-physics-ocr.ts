// Helper function to get a topic by ID
export function getOCRPhysicsTopicById(id: string): Topic | undefined {
  return ocrPhysicsTopics.find(topic => topic.id === id);
}

// Helper function to get topics by paper
export function getOCRPhysicsTopicsByPaper(paper: 1 | 2): Topic[] {
  return ocrPhysicsTopics.filter(topic => topic.paperRestriction === `Paper ${paper}`);
}

// Helper function to count all subtopics
export function countOCRPhysicsSubtopics(): number {
  return ocrPhysicsTopics.reduce((total, topic) => total + topic.subtopics.length, 0);
}
