export function getAQAALevelBiologyTopicById(id: string): Topic | undefined {
  return aqaALevelBiologyTopics.find((topic) => topic.id === id);
}

export function getAQAALevelBiologyTopicsByPaper(paper: 1 | 2): Topic[] {
  const paperName = `Paper ${paper}`;
  return aqaALevelBiologyTopics.filter((topic) => topic.paperRestriction === paperName);
}

export function countAQAALevelBiologySubtopics(): number {
  return aqaALevelBiologyTopics.reduce((total, topic) => total + topic.subtopics.length, 0);
}
